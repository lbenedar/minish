#include "libft.h"

int	ft_strcmp(char *str_1, char *str_2)
{
	int	i;

	i = 0;
	while ((str_1[i]) && (str_1[i] == str_2[i]))
		++i;
	return (str_1[i] - str_2[i]);
}
